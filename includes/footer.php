/**
 * Toggles footer link columns for mobile view (Accordion behavior)
 */
function toggleFooterCol(colIndex) {
  if (window.innerWidth > 768) return; // Expand always on desktop
  
  const col = document.getElementById(`js-footer-col-${colIndex}`);
  if (!col) return;

  const isOpen = col.classList.contains('open');
  
  // Close all columns first (optional, for accordion experience)
  const allCols = document.querySelectorAll('.footer-col');
  allCols.forEach(c => c.classList.remove('open'));
  
  if (!isOpen) {
    col.classList.add('open');
  }
}
